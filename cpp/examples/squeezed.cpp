#include "AmplitudeComputer.hpp"

#include <cstdlib>
#include <complex>
#include <iostream>
#include <vector>

int main() {
    const size_t modes = 6;

    std::vector<std::complex<double>> A{
        {-0.3368416, +0.63750624},  {-0.12145341, -0.11122608}, {-0.10751575, +0.20528171},
        {0.18271494, -0.02929658},  {0.02146285, -0.22311944},  {0.18775514, +0.14876147},
        {-0.12145341, -0.11122608}, {0.02895332, +0.09218518},  {0.12794796, -0.22207527},
        {0.54388722, +0.17472184},  {-0.46162299, -0.22275896}, {0.01440798, -0.22617286},
        {-0.10751575, +0.20528171}, {0.12794796, -0.22207527},  {0.62764364, -0.23086056},
        {0.07216265, +0.10401823},  {0.21788443, +0.33046886},  {0.02672011, -0.050208},
        {0.18271494, -0.02929658},  {0.54388722, +0.17472184},  {0.07216265, +0.10401823},
        {0.33127971, -0.20197803},  {0.28784343, -0.23154686},  {-0.06780801, +0.27266904},
        {0.02146285, -0.22311944},  {-0.46162299, -0.22275896}, {0.21788443, +0.33046886},
        {0.28784343, -0.23154686},  {0.18559416, -0.10202355},  {-0.30038116, -0.03704869},
        {0.18775514, +0.14876147},  {0.01440798, -0.22617286},  {0.02672011, -0.050208},
        {-0.06780801, +0.27266904}, {-0.30038116, -0.03704869}, {-0.4929831, +0.46584135}};

    std::vector<std::complex<double>> b(modes, {0.0, 0.0});

    std::vector<std::vector<std::complex<double>>> Gs;
    Gs.emplace_back(std::vector<std::complex<double>>{{0.1306161337423755,0.0}});

    auto ac = Mustard::AmplitudeComputer(modes);
    double norm_sqr = std::norm(Gs[0][0]);
    size_t n = 1;
    while(std::sqrt(norm_sqr) < 0.99) {
        Gs.emplace_back(std::vector<std::complex<double>>(ac.level_size(n), {0.0, 0.0}));
        Gs.emplace_back(std::vector<std::complex<double>>(ac.level_size(n+1), {0.0, 0.0}));
        norm_sqr += ac.fill_next_level(A, b, Gs[n-1], Gs[n], Gs[n+1], n);
        n += 2;
    }

    std::cout << n << std::endl;

    return 0;
}
